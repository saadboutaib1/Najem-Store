export function getAdminText(language, path, fallback = path) {
  const dictionary = adminDictionaries[language] || adminDictionaries.ar;
  const value = path.split('.').reduce((current, key) => current?.[key], dictionary);

  if (value !== undefined) {
    return value;
  }

  return path.split('.').reduce((current, key) => current?.[key], adminDictionaries.ar) ?? fallback;
}
